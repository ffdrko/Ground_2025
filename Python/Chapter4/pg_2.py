# Casting a number to a string
decade = str(1980)
print(type(decade))
print(decade)  # Output: 1980

statement = "I like rock music from the " + decade + "s."
print(statement)  # Output: I like rock music from the 1980s.

multiline = '''
Hey, I'm a multi-line string.
I can contain multiple lines of text.
'''
print(multiline)  # Output: Hey, I'm a multi-line string.